int	ft_str_is_lowercase(char *str)
{
	int	test;
	int	i;

	test = 1;
	i = 0;
	if (str[i] != '\0')
	{
		while (str[i] != '\0' && test == 1)
		{
			if (str[i] < 'a' || str[i] > 'z')
				test = 0;
			i++;
		}
	}
	return (test);
}
