int	ft_str_is_numeric(char *str)
{
	int	test;
	int	i;

	test = 1;
	i = 0;
	if (str[i] != '\0')
	{
		while (str[i] != '\0' && test == 1)
		{
			if (str[i] < '0' || str[i] > '9')
			{
				test = 0;
			}
			i++;
		}
	}
	return (test);
}
