int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	j;
	int	res;

	i = 0;
	j = 0;
	res = 0;
	while ((s1[i] != '\0' || s2[j] != '\0') && res == 0)
	{
		if (s1[i] < s2[j])
			res = -1;
		if (s1[i] > s2[j])
			res = 1;
		if (s1[i] == s2[j])
			res = 0;
		i++;
		j++;
	}
	return (res);
}
